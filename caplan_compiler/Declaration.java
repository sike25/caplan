// =================================================================================================================================
abstract public class Declaration {
// =================================================================================================================================



    // =================================================================================================================================
    // DATA MEMBERS

    /** The name of the declared item. */
    public final String   _name;

    /** The position in the source at which the declaration begins. */
    public final int      _position;

    /** The type of the declared item. */
    public final Type     _type;
    // =================================================================================================================================

    

    // =================================================================================================================================
    /**
     * Create a new variable declaration.
     *
     * @param token The token use declare the name.
     * @param type  The data type of the declared item.
     */
    public Declaration (Token token, Type type) {
	
	_name     = token._text;
	_type     = type;
	_position = token._position;
	
    } // Declaration ()
    // =================================================================================================================================



    // =================================================================================================================================
    /**
     * Generate aseembly of this delared item (if any).
     *
     * @return assembly defining this item.
     */
    abstract public String toAssembly();
    // =================================================================================================================================



    // =================================================================================================================================
    /**
     * Provide assembly for any statics contained in this declared item.
     *
     * @return assembly to define the static elements of this item.
     */
    abstract public String toStatics();
    // =================================================================================================================================

    

    // =================================================================================================================================
    public String toString () {
	
	return _name;
	
    } // toString ()
    // =================================================================================================================================


    
// =====================================================================================================================================
} // class Declaration
// =====================================================================================================================================
