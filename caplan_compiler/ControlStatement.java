// =================================================================================================================================
/**
 * A statement that controls the flow of the execution.
 */
abstract class ControlStatement extends Statement {
// =================================================================================================================================




    // =============================================================================================================================
    // DATA MEMBERS
    
    /**
     * The condition prompting a branch. E.g. while loops: the continuation condition, if-then statements: the execution condition.
     */
    public final Expression _condition;
    // =============================================================================================================================

	

    // =============================================================================================================================
    /**
     * Create a new control statement.
     *
     * @param position The position at which this statement begins in the source code.
     */
    public ControlStatement (int position, Expression condition) {
	
        super(position);
        this._condition = condition;
	
    } // ControlStatement ()
    // =============================================================================================================================


    
// =================================================================================================================================
} // class ControlStatement
// =================================================================================================================================
